public class NotUserVisitor {

    public static void main(String[] args) {
        System.out.println("男人成功时，背后多半有一个伟大的女人");
        System.out.println("女人成功时，背后多半有一个不成功的男人");
        System.out.println("男人失败时，喝闷酒谁也不用劝");
        System.out.println("女人失败时，两眼泪汪汪，谁也劝不了");
        System.out.println("男人恋爱时，凡事不动装懂");
        System.out.println("女人恋爱时，遇事懂也装不懂");
    }
}
